/*
*  Un operador es un simbolo que le dice al compilador
*  Que debe de realizar una tare 
*  Matematica, realicional o logica
*  Y debe de reproducir un resultado
*/

main(){

  int a = 10 + 5; //  + 15
  a = 20 - 10; //  - 10
  a = 10 * 2; // * 20

  double b = 10 / 2; // / 5
  b = 10.0 % 3;  // % 1 El sobrante de la division
  b = -b; // - expresion  'Es usado para cambiar el valor de la expresion


  int c = 10 ~/ 3; //  ~/  3 Divison comun y corriente pero solo la parte entera


  int d = 1; 
  d++; // ++ Incremento en uno el valor  ---- 2
  d--; // -- Decremento en uno el valor ---- 1
  d += 2; //  += 3
  d -= 2; // -= 1

}


